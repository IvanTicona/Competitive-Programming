#include<bits/stdc++.h>

using namespace std;

#define INF 1000000000
#define EPS 1e-9
#define MOD 1000000007
#define FOR(i, a, b) for(int i = a; i <= b; i++)
#define RFOR(i, a, b) for(int i = a; i >= b; i--)
#define FORP(i, a, b, c) for(int i = a; i <= b; i += c)
#define RFORP(i, a, b, c) for(int i = a; i >= b; i -= c)
#define F first
#define S second
#define all(x) x.begin(), x.end()
#define sort(x) sort(all(x))
#define sz(x) (int)x.size()
#define pb push_back
#define mp make_pair
#define rv(x) for(auto &i: x) cin >> i
#define includes(x, y) x.find(y) != x.end()
#define fill(x, y) memset(x, y, sizeof(x))
#define mxe(x) *max_element(all(x))
#define mne(x) *min_element(all(x))

typedef long long ll;
typedef unsigned long long ull;
typedef unsigned int ui;
typedef pair<int, int> ii;
typedef vector<int> vi;
typedef vector<string> vs;
typedef vector<bool> vb;
typedef vector<ll> vll;
typedef vector<vi> vvi;
typedef vector<ii> vii;

ll count(const vll &s, ll p) {
  return s.end() - lower_bound(all(s), p);
}

void solve(){

  ll n, k; cin >> n >> k;
  
  vll a(n), b(n); rv(a); rv(b);
  
  sort(a);
  sort(b);
  
  vll p;
  p.reserve(3*n); // reassign
  for(auto &x: a) p.pb(x);
  for(auto &x: b){
    p.pb(x);
    p.pb(x + 1);
  }
  
  sort(p);
  p.erase(unique(all(p)), p.end());
  
  ll max_earning = 0;
  
  for(auto &p: p){

    ll buy = count(b, p);
    ll pos = count(a, p);
    ll neg = buy - pos;
    
    if(neg <= k){
      ll earning = p * buy;
      if(earning > max_earning){
        max_earning = earning;
      }
    }
  }
  
  cout << max_earning << endl;
}

int main(){

  ios::sync_with_stdio(0);
  cin.tie(0); cout.tie(0);

  // freopen("input.txt", "r", stdin);
  // freopen("output.txt", "w", stdout);

  int t; cin >> t;
  while(t--){
    solve();
  }

  return 0;
}