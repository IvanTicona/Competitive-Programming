ll binsearch(const vll &a_sorted, ll K) {
  int n = a_sorted.size();
  ll count = 0;
  int left = 0, right = n - 1;
  while (left < right) {
    if (a_sorted[left] + a_sorted[right] <= K) {
      count += (right - left);
      left++;
    }
    else {
      right--;
    }
  }
  return count;
}

void solve(){

  ll n, x, y; cin >> n >> x >> y;
  vll a(n); rv(a);
  
  ll sum_a = 0;
  for(auto num: a) sum_a += num;
  
  ll L = sum_a - y;
  ll R = sum_a - x;
  
  sort(a);
  
  L = max(L, 2LL);
  R = min(R, 2000000000LL);
  
  if(L > R){
    cout << "0" << endl;
    return;
  }
  
  ll res_R = binsearch(a, R);
  ll res_L = (L > 2) ? binsearch(a, L - 1) : 0;
  cout << res_R - res_L << endl;
}

