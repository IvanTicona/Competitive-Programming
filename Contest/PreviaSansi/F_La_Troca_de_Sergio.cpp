void solve(){
  int n, k, q;
    cin >> n >> k >> q;

    vector<int> a(k), b(k);
    for (int i = 0; i < k; ++i) cin >> a[i];  // Posiciones de las señales
    for (int i = 0; i < k; ++i) cin >> b[i];  // Tiempos en las señales

    // Resolver las consultas
    while (q--) {
        int d;
        cin >> d;

        // Si d está antes de la primera señal
        if (d <= a[0]) {
            cout << b[0] << " ";
        }
        // Si d está después de la última señal
        else if (d >= a[k - 1]) {
            cout << b[k - 1] + (d - a[k - 1]) << " ";
        } else {
            // Buscar la señal más cercana a la izquierda de d
            int idx = lower_bound(a.begin(), a.end(), d) - a.begin();
            int i = idx - 1; // la señal antes de d
            int j = idx;     // la señal después de d

            // Interpolación lineal entre a[i] y a[j]
            int interpolated_time = b[i] + (b[j] - b[i]) * (d - a[i]) / (a[j] - a[i]);
            cout << interpolated_time << " ";
        }
    }
    cout << "\n";
}

