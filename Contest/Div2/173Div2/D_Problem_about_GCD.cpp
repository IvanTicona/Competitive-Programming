ll mygcd(ll a, ll b){
  while(b != 0){
    ll t = a % b;
    a = b;
    b = t;
  }
  return a;
}


ll safeAdd(ll a, ll b){
  return a + b; 
}

void solve(){

  ll l, r, G; cin >> l >> r >> G;

  ll L, Rint;
  if(G == 1){
    L = l;
    Rint = r;
  } else {
    if(r < G) {
      cout << "-1 -1" << endl;
      return;
    } else {
      Rint = r / G;
    }
    L = (l + (G-1)) / G; 
    if(L > Rint){
      cout << "-1 -1" << endl;
      return;
    }
  }
  if(L == Rint){
    ll M = L; 
    if(M == 1 && (l <= G && G <= r)){
      cout << G << " " << G << endl;
    } else {
      cout << "-1 -1" << endl;
    }
    return;
  }

  vll candX, candY;
  
  {
    ll endX = min(Rint, L+7);
    for(ll x=L; x<=endX; x++){
      candX.pb(x);
    }
  }
  {
    ll startY = (Rint >= 7? Rint-7 : 0); 
    if(startY < L) startY = L; 
    for(ll y=Rint; y>=startY; y--){
      candY.pb(y);
    }
  }
  
  sort(candX);
  candX.erase(unique(all(candX)), candX.end());
  sort(candY);
  candY.erase(unique(all(candY)), candY.end());

  ll bestDist = -1;
  ll bestx = -1, besty = -1;
  for(ll x : candX){
    for(ll y : candY){
      if(y < x) continue;
      ll g = mygcd(x, y);
      if(g == 1){
        ll dist = y - x;
        if(dist > bestDist){
          bestDist = dist;
          bestx = x;
          besty = y;
        } else if(dist == bestDist && x < bestx){
          bestx = x;
          besty = y;
        }
      }
    }
  }

  if(bestDist < 0){
    cout << "-1 -1" << endl;
  } else {
    ll A = (G == 1 ? bestx : bestx * G);
    ll B = (G == 1 ? besty : besty * G);
    cout << A << " " << B << endl;
  }
}

