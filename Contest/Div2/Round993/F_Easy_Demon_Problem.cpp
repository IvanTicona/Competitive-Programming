void solve(){

  int n, m, q;
    cin >> n >> m >> q;

    vector<long long> a(n), b(m);
    for (int i = 0; i < n; ++i) cin >> a[i];
    for (int j = 0; j < m; ++j) cin >> b[j];

    vector<long long> queries(q);
    for (int i = 0; i < q; ++i) cin >> queries[i];

    // Precomputación
    long long sumA = 0, sumB = 0;
    for (int i = 0; i < n; ++i) sumA += a[i];
    for (int j = 0; j < m; ++j) sumB += b[j];

    // Usar long long para evitar desbordamientos
    long long totalBeauty = sumA * sumB;

    // Conjuntos para búsqueda eficiente
    unordered_set<long long> setB(b.begin(), b.end());

    // Responder consultas
    for (long long x : queries) {
        bool possible = false;

        // Intentar cada valor de a_r
        for (long long a_r : a) {
            // Resolver para b_c:
            // b_c = (x - totalBeauty + a_r * sumB) / (a_r + sumA)
            long long num = x - totalBeauty + a_r * sumB;
            long long denom = sumA + a_r;

            // Verificar si la división es exacta y b_c está en b
            if (denom != 0 && num % denom == 0) {
                long long b_c = num / denom;
                if (setB.count(b_c)) {
                    possible = true;
                    break;
                }
            }
        }

        cout << (possible ? "YES\n" : "NO\n");
    }

}

