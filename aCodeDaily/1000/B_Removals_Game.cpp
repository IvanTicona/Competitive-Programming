typedef vector<vi> vvi;
typedef vector<ii> vii;

bool canWin(vi &a, vi &b, int n){
  for (int i = 0, j = n - 1; i < n; i++, j--)
  {
    if(a[i] != b[j]){
      return false;
    }
  }
  return true;
}

bool revWin(vi &a, vi &b, int n){
  for (int i = 0; i < n; i++)
  {
    if(a[i] != b[i]){
      return false;
    }
  }
  return true;
}

void solve(){

  int n; cin >> n;
  vi a(n); rv(a);
  vi b(n); rv(b);
  
  if(canWin(a, b, n) || revWin(a, b, n)) cout << "Bob\n";
  else cout << "Alice\n";
}

