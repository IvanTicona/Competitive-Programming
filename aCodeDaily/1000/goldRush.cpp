void f(int n, set<int> &nums){
  if(!(n%3 == 0)) return;
  int v = n/3;
  nums.insert(v);
  nums.insert(v*2);
  f(v, nums);
  f(v*2, nums);
}

void solve(){
  int n, m; cin >> n >> m;
  if(n == m){
    cout << "YES" << endl;
    return;
  }
  if(m > n || !(n%3 == 0)){
    cout << "NO" << endl;
    return;
  }
  set<int> nums;
  f(n, nums);
  if(includes(nums, m)) cout << "YES" << endl;
  else cout << "NO" << endl;
}

